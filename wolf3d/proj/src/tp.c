#include <wolf.h>

void	ft_tp(t_event *e)
{
	if (e->s->map[(int)e->c->posx][(int)e->c->posy] == '!')
	{
		if ((int)e->c->posx == e->s->tp[0][0] &&
			(int)e->c->posy == e->s->tp[0][0])
		{
			e->c->posx = e->s->tp[1][0] + 1;
			e->c->posy = e->s->tp[1][1] + 1;
		}
		else
		{
			e->c->posx = e->s->tp[0][0] + 1;
			e->c->posy = e->s->tp[0][0] + 1;
		}
	}
}
